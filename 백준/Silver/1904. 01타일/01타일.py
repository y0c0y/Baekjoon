# 백준 1904번 문제 - 01타일

# n = 1 -> 1
# n = 2 -> 00 11
# n = 3 -> 001 100 111
# n = 4 -> 0000 0011 1100 1001 1111
# n = 5 -> 00001 00100 10000 00111 10011 11001 11111 00000
# n = 6 -> 000000 000011 001100 100001 110000 001111 100111 110011 111111
# 피보나치 수열과 같은 규칙을 가지고 있음

import sys
input = sys.stdin.readline

n = int(input())
dp = [0] * 1000001
dp[1] = 1
dp[2] = 2

for k in range(3,n+1):
    dp[k] = (dp[k-1]+ dp[k-2])%15746
print(dp[n])